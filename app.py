#!/usr/bin/env python3
"""Обучающее CLI-приложение по торговле на основе объёмов.

Важно: это не финансовая рекомендация.
"""

from __future__ import annotations

from dataclasses import dataclass
from textwrap import dedent


@dataclass(frozen=True)
class Setup:
    name: str
    market_context: str
    entry_rule: str
    stop_rule: str
    take_profit_rule: str
    invalidation: str


SETUPS: tuple[Setup, ...] = (
    Setup(
        name="Пробой уровня на повышенном объёме",
        market_context=(
            "Цена долго консолидируется у уровня сопротивления/поддержки, "
            "а затем выходит из диапазона."
        ),
        entry_rule=(
            "Входить только если объём на пробойной свече минимум в 1.5–2 раза "
            "выше среднего объёма за последние 20 свечей."
        ),
        stop_rule=(
            "Стоп — за противоположную границу пробойной свечи или за уровень, "
            "который был пробит."
        ),
        take_profit_rule=(
            "Частичная фиксация на 1R, основная цель 2R–3R или следующий "
            "ключевой уровень ликвидности."
        ),
        invalidation=(
            "Если цена вернулась обратно в диапазон и объём на возврате высокий, "
            "сценарий пробоя считается слабым."
        ),
    ),
    Setup(
        name="Кульминация объёма (возможный разворот)",
        market_context=(
            "После сильного тренда появляется очень большой объём на свече с "
            "длинной тенью или слабым продолжением движения."
        ),
        entry_rule=(
            "Ждать подтверждения: следующая свеча должна показать отказ от "
            "продолжения тренда (например, закрыться против импульса)."
        ),
        stop_rule=(
            "Стоп за экстремум кульминационной свечи. Риск на сделку не более 1% "
            "от депозита."
        ),
        take_profit_rule=(
            "Первая цель — возврат к средней зоне диапазона, вторая — противоположная "
            "граница диапазона/структуры."
        ),
        invalidation=(
            "Если после кульминации цена обновляет экстремум на ещё большем объёме, "
            "разворотный сценарий отменяется."
        ),
    ),
    Setup(
        name="Откат в тренде с низким объёмом",
        market_context=(
            "В устойчивом тренде откат проходит на снижающемся объёме — это может "
            "говорить о слабости контрдвижения."
        ),
        entry_rule=(
            "Искать вход в сторону тренда, когда после отката появляется свеча "
            "с ростом объёма и подтверждением структуры (higher low / lower high)."
        ),
        stop_rule="Стоп за локальный минимум/максимум отката.",
        take_profit_rule=(
            "Фиксировать прибыль ступенчато: 1R, 2R и трейлинг-стоп по структуре."
        ),
        invalidation=(
            "Если объём на откате резко растёт и цена ломает трендовую структуру, "
            "сетап отменяется."
        ),
    ),
)


def print_intro() -> None:
    print(
        dedent(
            """
            ============================================
            Volume Trading Helper (обучающий режим)
            ============================================
            Это приложение объясняет базовые сетапы торговли по объёмам.
            Не является инвестиционной рекомендацией.
            """
        ).strip()
    )


def print_risk_block() -> None:
    print(
        dedent(
            """

            Управление риском:
            1) Риск на сделку: 0.5%–1% от капитала.
            2) Обязательный стоп-лосс для каждой сделки.
            3) Не открывать больше 2–3 коррелирующих позиций одновременно.
            4) Вести журнал сделок (контекст, объём, результат, ошибки).
            """
        ).rstrip()
    )


def print_setup(setup: Setup, idx: int) -> None:
    print(f"\n[{idx}] {setup.name}")
    print(f"  Контекст: {setup.market_context}")
    print(f"  Вход: {setup.entry_rule}")
    print(f"  Стоп: {setup.stop_rule}")
    print(f"  Тейк-профит: {setup.take_profit_rule}")
    print(f"  Отмена сценария: {setup.invalidation}")


def main() -> None:
    print_intro()
    for i, setup in enumerate(SETUPS, start=1):
        print_setup(setup, i)
    print_risk_block()


if __name__ == "__main__":
    main()
